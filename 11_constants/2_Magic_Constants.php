<?php
// Magic constants represent information that can be accessed about the file and script itself.
// Returns the current line number in the file
echo __LINE__;
// Returns the file's full path
echo __FILE__;
// Returns the directory of the file
echo __DIR__;
// Returns the function name that the code is executing in
echo __FUNCTION__;
// Returns the class name
echo __CLASS__;
// Returns the trait name
echo __TRAIT__;
// Returns the class method name
echo __METHOD__;
// Returns the namespace name
echo __NAMESPACE__;
